function parseSafeInteger(value: string, code: string) {
  if (!/^-?\d+$/.test(value)) failForestLayout(code);
  const parsed = Number(value);
  if (!Number.isSafeInteger(parsed)) failForestLayout(code);
  return parsed;
}

